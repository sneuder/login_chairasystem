//When the fiel text is focused
function focus_iconColor1(){
    document.getElementById("icon1").style.color = "#009a25";
    document.getElementById("user").style.borderBottomColor = "#009a25";
}

//When the fiel text isnt focused
function focusout_iconColor1(){
    document.getElementById("icon1").style.color = "#d2d2d2";
    document.getElementById("user").style.borderBottomColor = "#d2d2d2";
}

